def main():
    print("=" * 80)
    print("  EU DPP PLATFORM — DEMO SEED & FUNCTIONAL TEST")
    print(f"  Target: {API_URL}")
    print("=" * 80)

    # Auth
    print("\n  0. AUTHENTICATION")
    token = get_token()
    if token:
        print(f"    [PASS] Token obtained for {AUTH_USER}")
    else:
        print(f"    [WARN] No token — proceeding without auth (some endpoints may fail)")

    t0 = time.time()

    test_health(token)
    test_dpp_sectors(token)
    dpp_requests = seed_dpp_products(token)
    test_dpp_get(token, dpp_requests)
    test_dpp_list_by_sector(token)
    battery_payloads = seed_batteries(token)
    test_battery_get(token, battery_payloads)
    test_supply_chain(token, dpp_requests)
    test_qr_carrier(token, dpp_requests)
    test_compliance(token)
    test_ml_predictions(token)
    test_lifecycle(token)
    test_audit_log(token)
    test_human_review(token)
    test_workflow(token)

    elapsed = time.time() - t0

    # Report
    passed = sum(1 for r in results if r["ok"])
    failed = sum(1 for r in results if not r["ok"])

    print(f"\n{'=' * 80}")
    print(f"  TEST REPORT")
    print(f"{'=' * 80}")
    print(f"  Total tests:  {len(results)}")
    print(f"  Passed:       {passed}")
    print(f"  Failed:       {failed}")
    print(f"  Duration:     {elapsed:.1f}s")
    print(f"  Pass rate:    {passed / len(results) * 100:.1f}%")

    if failed > 0:
        print(f"\n  FAILED TESTS:")
        for r in results:
            if not r["ok"]:
                print(f"    - {r['test']}: {r['method']} {r['url']} → {r['status']} {r['detail'][:60]}")

    # Save report
    report_path = DEMO_DIR / "test_report.json"
    with open(report_path, "w") as f:
        json.dump({
            "api_url": API_URL, "total": len(results),
            "passed": passed, "failed": failed,
            "duration_s": round(elapsed, 1),
            "results": results,
        }, f, indent=2)
    print(f"\n  Report saved: {report_path}")
    print(f"{'=' * 80}\n")

    return 0 if failed == 0 else 1


if __name__ == "__main__":
    sys.exit(main())
