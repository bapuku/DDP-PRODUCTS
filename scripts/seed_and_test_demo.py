#!/usr/bin/env python3
"""
Seed demo data into the production API and test all endpoints.

Reads the demo JSON files generated by generate_demo_data.py, then:
  1. Seeds products into Neo4j via the sector API
  2. Creates battery passports via the battery API
  3. Tests compliance, ML, lifecycle, supply-chain, and audit endpoints
  4. Produces a test report

Usage:
  python scripts/seed_and_test_demo.py                     # default: http://51.77.144.54:8100
  API_URL=http://localhost:8000 python scripts/seed_and_test_demo.py  # local
"""
import json
import os
import sys
import time
from pathlib import Path

try:
    import requests
except ImportError:
    print("pip install requests")
    sys.exit(1)

API_URL = os.environ.get("API_URL", "http://51.77.144.54:8100")
DEMO_DIR = Path(__file__).resolve().parent.parent / "data" / "demo"

AUTH_USER = os.environ.get("AUTH_USER", "operator@example.com")
AUTH_PASS = os.environ.get("AUTH_PASS", "demo-password")

results: list[dict] = []


def load_json(name: str):
    p = DEMO_DIR / name
    if not p.exists():
        print(f"  MISSING: {p} — run generate_demo_data.py first")
        return []
    with open(p) as f:
        return json.load(f)


def get_token() -> str | None:
    try:
        r = requests.post(
            f"{API_URL}/api/v1/auth/token",
            data={"username": AUTH_USER, "password": AUTH_PASS},
            timeout=10,
        )
        if r.status_code == 200:
            return r.json().get("access_token")
    except Exception:
        pass
    return None


def headers(token: str | None) -> dict:
    h = {"Content-Type": "application/json"}
    if token:
        h["Authorization"] = f"Bearer {token}"
    return h


def record(name: str, method: str, url: str, status: int, ok: bool, detail: str = ""):
    results.append({
        "test": name, "method": method, "url": url,
        "status": status, "ok": ok, "detail": detail[:200],
    })
    icon = "PASS" if ok else "FAIL"
    print(f"    [{icon}] {method} {url} → {status} {detail[:80]}")


def test_health(token):
    print("\n  1. HEALTH CHECKS")
    for path in ["/health", "/ready"]:
        try:
            r = requests.get(f"{API_URL}{path}", timeout=10)
            record(f"health:{path}", "GET", path, r.status_code,
                   r.status_code == 200, r.text[:100])
        except Exception as e:
            record(f"health:{path}", "GET", path, 0, False, str(e))


def test_dpp_sectors(token):
    print("\n  2. DPP SECTORS LIST")
    try:
        r = requests.get(f"{API_URL}/api/v1/dpp", headers=headers(token), timeout=10)
        record("dpp:sectors", "GET", "/api/v1/dpp", r.status_code,
               r.status_code == 200, r.text[:100])
    except Exception as e:
        record("dpp:sectors", "GET", "/api/v1/dpp", 0, False, str(e))


def seed_dpp_products(token):
    print("\n  3. SEED DPP PRODUCTS (sector API)")
    dpp_requests = load_json("demo_dpp_create_requests.json")
    ok_count = 0
    for i, req in enumerate(dpp_requests):
        try:
            r = requests.post(
                f"{API_URL}/api/v1/dpp/sector",
                headers=headers(token),
                json=req,
                timeout=30,
            )
            ok = r.status_code in (200, 201, 409)
            if ok:
                ok_count += 1
            record(f"seed:dpp:{i}", "POST", "/api/v1/dpp/sector",
                   r.status_code, ok,
                   f"{req['sector']}/{req['gtin']} → {r.text[:60]}")
        except Exception as e:
            record(f"seed:dpp:{i}", "POST", "/api/v1/dpp/sector", 0, False, str(e))
    print(f"    → {ok_count}/{len(dpp_requests)} products seeded")
    return dpp_requests


def test_dpp_get(token, dpp_requests):
    print("\n  4. GET DPP PRODUCTS")
    for req in dpp_requests[:5]:
        sector = req["sector"]
        gtin = req["gtin"]
        serial = req["serial_number"]
        path = f"/api/v1/dpp/sector/{sector}/{gtin}/{serial}"
        try:
            r = requests.get(f"{API_URL}{path}", headers=headers(token), timeout=10)
            record(f"get:dpp:{sector}", "GET", path, r.status_code,
                   r.status_code == 200, r.text[:80])
        except Exception as e:
            record(f"get:dpp:{sector}", "GET", path, 0, False, str(e))


